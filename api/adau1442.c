static int adau1442_write(unsigned short reg, void *data, unsigned int len)
{
	i2c_ctrl_t ctrl;

	memset(&ctrl, 0x00, sizeof(i2c_ctrl_t));
	ctrl.slave_addr = ADDR_ADAU1442;
	ctrl.reg_addr = reg;
	ctrl.reg_addr_len = I2C_ADDRESS_2BYTE;
	memcpy(ctrl.buf, data, len);
	ctrl.buf_len = len;

	if (write_i2c(&adau1442, &ctrl) < 0) {
		dlp("write_i2c() fail");
		return -1;
	}
	return 0;
}

