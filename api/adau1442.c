int adau1442_register(dsp_adapter_t *adapter)
{
	memset(&adau1442, 0x00, sizeof(i2c_t));

	adau1442.dev = PATH_DEV_I2C_CH1;

	if (open_i2c(&adau1442) < 0) {
		dlp_err("open_i2c() fail");
		return -1;
	}

	if ((gpioctrl_read(ADAU1442_SB_FINISH_FB) != GPIO_LOW) ||
		(gpioctrl_read(ADAU1442_ACT_STATUS_FB) != GPIO_LOW)) {
		dlp_err("adau1442 dsp is not boot or running!");
		return -1;
	}

	if (adau1442_set_dsp_sel(ADAU_DSP_SEL_DISABLE) < 0) {
		dlp_err("adau1442 dsp select mode set fail!");
		return -1;
	}

	if (adau1442_set_output_sel(ADAU_OUTSEL_DIS_DSP) < 0) {
		dlp_err("adau1442 dsp output select mode set fail!");
		return -1;
	}

	adapter = &adau1442_dsp;

	return 0;
}

