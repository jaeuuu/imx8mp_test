static int adau1442_set_dsp_sel(char mode)
{
	unsigned short reg;
	unsigned int val;

	reg = ADAUREG_DSP_SEL1;

	switch (mode) {
	case ADAU_DSP_SEL_DISABLE:
		val = ADAUVAL_DSP_DISABLE1;
		break;
	case ADAU_DSP_SEL_ENABLE:
		val = ADAUVAL_DSP_ENABLE1;
		break;
	default:
		return -1;
	}

	if (adau1442_write(reg, &val, ADAUVAL_DSP_SIZE) < 0)
		return -1;

	reg = ADAUREG_DSP_SEL2;

	switch (mode) {
	case ADAU_DSP_SEL_DISABLE:
		val = ADAUVAL_DSP_DISABLE2;
		break;
	case ADAU_DSP_SEL_ENABLE:
		val = ADAUVAL_DSP_ENABLE2;
		break;
	default:
		return -1;
	}

	if (adau1442_write(reg, &val, ADAUVAL_DSP_SIZE) < 0)
		return -1;
	return 0;
}

